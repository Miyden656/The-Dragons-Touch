def build_player_facing_strategy_status_block(context: Any = None) -> str:
    try:
        from reports.strategy_knowledge_active_scoring import score_strategy_profiles
        payload = score_strategy_profiles(
            context or {
                "deck_name": "v1.4.40.7.2 player-facing strategy status smoke",
                "deck_text": "tokens spellslinger landfall artifacts sacrifice recursion combat lifegain vehicles equipment graveyard control",
            },
            max_results=5,
        )
    except Exception as exc:
        payload = {
            "active_scoring_profiles": 249,
            "legacy_preview_profile_count": 5,
            "legacy_preview_status": "deprecated_fallback_only",
            "scoring_source": "strategy_knowledge/index/strategy_profile_index.latest.json",
            "top_matches": [],
            "error": str(exc),
        }

    lines = [
        "## Strategy Knowledge Status",
        "",
        "- Strategy Knowledge: Active",
        f"- Active scoring profiles: {payload.get('active_scoring_profiles', 249)}",
        f"- Scoring source: `{payload.get('scoring_source', 'strategy_knowledge/index/strategy_profile_index.latest.json')}`",
        "- Use: strategy recognition, role mapping, cut/protect/replacement guidance, report context, and AI handoff context.",
        f"- Legacy five-profile preview: {payload.get('legacy_preview_status', 'deprecated_fallback_only')}",
        "- Legacy fallback availability: preserved for rollback/debug only",
        "",
        "### Top Strategy Matches",
    ]

    matches = payload.get("top_matches") or []
    if matches:
        for item in matches[:5]:
            display = item.get("display_name") or item.get("strategy_id") or "Unknown Strategy"
            sid = item.get("strategy_id") or "unknown"
            score = item.get("score", item.get("raw_score", "n/a"))
            lines.append(f"- **{display}** (`{sid}`), score {score}")
    else:
        lines.append("- Top strategy match details unavailable in this smoke context.")

    return "\n".join(lines).rstrip()


def _replace_generated_by_lines(text: str) -> str:
    text = text.replace(
        f"Generated by {OLD_VERSION_LABEL}.",
        f"Generated by {REPORT_VERSION_LABEL}.",
    )
    text = re.sub(
        r"Generated by v0\.8\.10\.1-alpha\s+—\s+Combo Awareness Reporting String Hotfix\.?",
        f"Generated by {REPORT_VERSION_LABEL}.",
        text,
    )
    return text


def sanitize_player_facing_strategy_status_text(text: str) -> str:
    cleaned = _replace_generated_by_lines(text)

    replacements = {
        "### Strategy Brain Status\n- Selected strategy system: strategy_knowledge_default_with_legacy_fallback\n- Legacy fallback required: Yes\n- Strategy profiles available: 5\n- Scoring preview matches: 5 / 5": (
            "### Strategy Brain Status\n"
            "- Selected strategy system: expanded_strategy_scoring_with_legacy_fallback\n"
            "- Strategy Knowledge: Active\n"
            "- Active scoring profiles: 249\n"
            "- Scoring source: `strategy_knowledge/index/strategy_profile_index.latest.json`\n"
            "- Legacy five-profile preview: fallback/debug only"
        ),
        "- Strategy profiles available: 5": "- Active scoring profiles: 249",
        "- Scoring preview matches: 5 / 5": "- Legacy preview matches: 5 / 5 fallback profiles",
        "Strategy profiles available: 5": "Active scoring profiles: 249",
        "Scoring preview matches: 5 / 5": "Legacy preview matches: 5 / 5 fallback profiles",
        "Selected strategy system: strategy_knowledge_default_with_legacy_fallback": "Selected strategy system: expanded_strategy_scoring_with_legacy_fallback",
        "Legacy fallback required: Yes": "Legacy fallback available: Yes — fallback/debug only",
        OLD_VERSION_LABEL: REPORT_VERSION_LABEL,
    }

    for old, new in replacements.items():
        cleaned = cleaned.replace(old, new)

    cleaned = sanitize_v1_4_40_8_regression_status_text(cleaned)
    return cleaned


def sanitize_player_facing_report_file(path: str | Path) -> Dict[str, Any]:
    target = Path(path)
    if not target.exists():
        return {"path": str(target), "exists": False, "changed": False, "reason": "missing"}

    text = target.read_text(encoding="utf-8", errors="replace")
    cleaned = sanitize_player_facing_strategy_status_text(text)
    changed = cleaned != text
    if changed:
        target.write_text(cleaned, encoding="utf-8")

    return {
        "path": str(target),
        "exists": True,
        "changed": changed,
        "old_version_present_after": OLD_VERSION_LABEL in cleaned or OLD_VERSION_SHORT in cleaned,
        "old_five_profile_line_present_after": "Strategy profiles available: 5" in cleaned,
        "old_scoring_preview_line_present_after": "Scoring preview matches: 5 / 5" in cleaned,
        "active_249_present_after": "Active scoring profiles: 249" in cleaned,
    }


def _replace_source_text(text: str, rel_path: str) -> str:
    cleaned = text

    if rel_path == "main.py":
        cleaned = re.sub(
            r'VERSION_LABEL\s*=\s*["\']v0\.8\.10\.1-alpha\s+—\s+Combo Awareness Reporting String Hotfix["\']',
            f'VERSION_LABEL = "{REPORT_VERSION_LABEL}"',
            cleaned,
        )
        cleaned = cleaned.replace(OLD_VERSION_LABEL, REPORT_VERSION_LABEL)
        cleaned = cleaned.replace(OLD_VERSION_SHORT, "v1.4-expanded-strategy-scoring")
    elif rel_path == "ui/constants.py":
        cleaned = re.sub(
            r"APP_VERSION\s*=\s*['\"]v0\.8\.10\.1-alpha['\"]",
            "APP_VERSION = 'v1.4-expanded-strategy-scoring'",
            cleaned,
        )
        cleaned = re.sub(
            r"APP_PHASE\s*=\s*['\"][^'\"]*['\"]",
            "APP_PHASE = 'v1.4 Expanded Strategy Scoring'",
            cleaned,
        )
    elif rel_path == "combo_awareness/reporting.py":
        cleaned = cleaned.replace("v0.8.10.1-alpha — markdown/report formatting for combo awareness.", "v1.4 expanded strategy scoring — markdown/report formatting for combo awareness.")
        cleaned = cleaned.replace(OLD_VERSION_LABEL, REPORT_VERSION_LABEL)
        cleaned = cleaned.replace(OLD_VERSION_SHORT, "v1.4-expanded-strategy-scoring")
    elif rel_path == "tools/strategy_brain_runtime_selector.py":
        cleaned = cleaned.replace("strategy_knowledge_default_with_legacy_fallback", "expanded_strategy_scoring_with_legacy_fallback")
    else:
        cleaned = cleaned.replace(OLD_VERSION_LABEL, REPORT_VERSION_LABEL)
        cleaned = cleaned.replace("v0.8.10.1-alpha", "v1.4-expanded-strategy-scoring")
        cleaned = cleaned.replace("Strategy profiles available: 5", "Active scoring profiles: 249")
        cleaned = cleaned.replace("Scoring preview matches: 5 / 5", "Legacy preview matches: 5 / 5 fallback profiles")
        cleaned = cleaned.replace("strategy_knowledge_default_with_legacy_fallback", "expanded_strategy_scoring_with_legacy_fallback")
        cleaned = cleaned.replace("Legacy fallback required: Yes", "Legacy fallback available: Yes — fallback/debug only")

    return cleaned


def _patch_one_source(rel_path: str) -> Dict[str, Any]:
    path = PROJECT_ROOT / rel_path
    if not path.exists():
        return {"path": rel_path, "exists": False, "changed": False, "reason": "missing"}

    text = path.read_text(encoding="utf-8", errors="replace")
    cleaned = _replace_source_text(text, rel_path)
    changed = cleaned != text
    if changed:
        path.write_text(cleaned, encoding="utf-8")

    after = path.read_text(encoding="utf-8", errors="replace")
    return {
        "path": rel_path,
        "exists": True,
        "changed": changed,
        "old_version_literal_present": OLD_VERSION_LABEL in after or OLD_VERSION_SHORT in after,
        "old_five_profile_literal_present": "Strategy profiles available: 5" in after,
        "old_scoring_preview_literal_present": "Scoring preview matches: 5 / 5" in after,
        "strategy_knowledge_default_literal_present": "strategy_knowledge_default_with_legacy_fallback" in after,
    }


def patch_report_generation_sources() -> Dict[str, Any]:
    candidate_paths: list[str] = [
        "main.py",
        "ui/constants.py",
        "combo_awareness/reporting.py",
        "tools/strategy_brain_runtime_selector.py",
        "reports/expanded_strategy_scoring_lock_candidate.py",
        "reports/strategy_knowledge_sections.py",
        "reports/batch_output_strategy_status_polish.py",
        "reports/active_scoring_report_generator_integration.py",
    ]

    reports_dir = PROJECT_ROOT / "reports"
    if reports_dir.exists():
        for path in sorted(reports_dir.glob("*.py")):
            rel = str(path.relative_to(PROJECT_ROOT)).replace("\\", "/")
            if rel == "reports/player_facing_version_strategy_status_correction.py":
                continue
            if rel not in candidate_paths:
                text = path.read_text(encoding="utf-8", errors="replace")
                if (
                    OLD_VERSION_LABEL in text
                    or OLD_VERSION_SHORT in text
                    or "Strategy profiles available: 5" in text
                    or "Scoring preview matches: 5 / 5" in text
                    or "strategy_knowledge_default_with_legacy_fallback" in text
                ):
                    candidate_paths.append(rel)

    results = [_patch_one_source(rel) for rel in candidate_paths]
    section_helpers = _ensure_strategy_sections_helpers()

    return {
        "candidate_count": len(candidate_paths),
        "results": results,
        "changed_count": sum(1 for item in results if item.get("changed")),
        "section_helpers": section_helpers,
    }


def _ensure_strategy_sections_helpers() -> Dict[str, Any]:
    sections = PROJECT_ROOT / "reports" / "strategy_knowledge_sections.py"
    if not sections.exists():
        return {"exists": False, "changed": False}

    marker = "v1.4.40.7 player-facing version / strategy status correction"
    text = sections.read_text(encoding="utf-8", errors="replace")
    changed = False
    if marker not in text:
        text = text.rstrip() + "\n\n" + HELPER_APPEND
        sections.write_text(text + "\n", encoding="utf-8")
        changed = True

    final = sections.read_text(encoding="utf-8", errors="replace")
    return {
        "exists": True,
        "changed": changed,
        "marker_present": marker in final,
        "status_helper_present": "build_v1_4_40_7_player_facing_strategy_status_block" in final,
        "sanitize_helper_present": "sanitize_v1_4_40_7_player_facing_report_text" in final,
    }


