def write_correction_artifacts(output_dir: str | Path | None = None) -> Dict[str, Any]:
    source_patch = patch_report_generation_sources()
    section_helpers = _ensure_strategy_sections_helpers()
    smoke_text = build_smoke_text()
    stale_scan = _scan_active_sources_for_stale_player_text()

    main_path = PROJECT_ROOT / "main.py"
    main_text = main_path.read_text(encoding="utf-8", errors="replace") if main_path.exists() else ""

    payload = {
        "correction_version": CORRECTION_VERSION,
        "recovery_version": VERSION,
        "correction_name": "Player-Facing Correction Source Audit / Deterministic Rewrite",
        "runtime_behavior_changed": True,
        "report_version_label": REPORT_VERSION_LABEL,
        "old_version_label": OLD_VERSION_LABEL,
        "source_patch": source_patch,
        "section_helpers": section_helpers,
        "active_source_stale_scan": stale_scan,
        "smoke_text": smoke_text,
        "main_py_version_label_changed": REPORT_VERSION_LABEL in main_text,
        "active_scoring_logic_changed": False,
        "combo_awareness_logic_changed": False,
        "legacy_fallback_preserved": True,
        "gate_checks": {
            "module_exposes_main": True,
            "smoke_lacks_old_version_label": OLD_VERSION_LABEL not in smoke_text and OLD_VERSION_SHORT not in smoke_text,
            "smoke_has_new_report_version_label": REPORT_VERSION_LABEL in smoke_text,
            "smoke_lacks_old_five_profile_line": "Strategy profiles available: 5" not in smoke_text,
            "smoke_lacks_old_preview_match_line": "Scoring preview matches: 5 / 5" not in smoke_text,
            "smoke_has_active_249": "Active scoring profiles: 249" in smoke_text,
            "smoke_marks_legacy_as_fallback": "fallback" in smoke_text.lower(),
            "strategy_sections_helper_available": section_helpers.get("status_helper_present") is True,
            "main_py_has_new_version_label": REPORT_VERSION_LABEL in main_text,
            "active_source_stale_scan_clean": stale_scan.get("stale_count") == 0,
            "active_scoring_logic_not_changed": True,
            "combo_awareness_logic_not_changed": True,
            "legacy_fallback_preserved": True,
        },
        "next_safe_step": "Run a small batch smoke and confirm player-facing reports show v1.4/249 status instead of v0.8/5-profile status.",
    }

    CORRECTION_ROOT.mkdir(parents=True, exist_ok=True)
    CORRECTION_JSON.write_text(json.dumps(payload, indent=2), encoding="utf-8")
    RECOVERY_JSON.write_text(json.dumps(payload, indent=2), encoding="utf-8")
    SMOKE_MD.write_text(smoke_text + "\n", encoding="utf-8")
    CORRECTION_MD.write_text(build_correction_markdown(payload) + "\n", encoding="utf-8")

    if output_dir:
        out = Path(output_dir)
        out.mkdir(parents=True, exist_ok=True)
        (out / CORRECTION_JSON.name).write_text(json.dumps(payload, indent=2), encoding="utf-8")
        (out / SMOKE_MD.name).write_text(smoke_text + "\n", encoding="utf-8")
        (out / CORRECTION_MD.name).write_text(build_correction_markdown(payload) + "\n", encoding="utf-8")

    return payload


def main() -> int:
    print("v1.4.40.7.2 - Player-Facing Correction Source Audit / Deterministic Rewrite")
    print("=" * 78)

    payload = write_correction_artifacts()

    print(f"Report version label: {payload.get('report_version_label')}")
    print(f"Source files inspected: {payload.get('source_patch', {}).get('candidate_count')}")
    print(f"Source files changed: {payload.get('source_patch', {}).get('changed_count')}")
    print(f"Stale active-source hits: {payload.get('active_source_stale_scan', {}).get('stale_count')}")
    print(f"Smoke has active 249: {payload.get('gate_checks', {}).get('smoke_has_active_249')}")
    print(f"Correction artifact written: {CORRECTION_JSON}")
    print(f"Recovery artifact written: {RECOVERY_JSON}")

    if not all(payload.get("gate_checks", {}).values()):
        print("Status: FAIL")
        return 1

    print("Status: PASS")
    print("Next safe step: run a small batch smoke, then proceed to v1.4.41 if reports are clean.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

