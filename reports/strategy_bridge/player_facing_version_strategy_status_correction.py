def _scan_active_sources_for_stale_player_text() -> Dict[str, Any]:
    scan_paths = [
        "main.py",
        "ui/constants.py",
        "combo_awareness/reporting.py",
        "tools/strategy_brain_runtime_selector.py",
        "reports/expanded_strategy_scoring_lock_candidate.py",
        "reports/strategy_knowledge_sections.py",
        "reports/batch_output_strategy_status_polish.py",
        "reports/active_scoring_report_generator_integration.py",
    ]

    stale: list[dict[str, Any]] = []
    for rel in scan_paths:
        path = PROJECT_ROOT / rel
        if not path.exists():
            continue
        text = path.read_text(encoding="utf-8", errors="replace")
        hits = []
        if OLD_VERSION_LABEL in text or OLD_VERSION_SHORT in text:
            hits.append("old_version")
        if "Strategy profiles available: 5" in text:
            hits.append("old_five_profile_line")
        if "Scoring preview matches: 5 / 5" in text:
            hits.append("old_scoring_preview_line")
        if rel == "tools/strategy_brain_runtime_selector.py" and "strategy_knowledge_default_with_legacy_fallback" in text:
            hits.append("old_strategy_mode_label")
        if hits:
            stale.append({"path": rel, "hits": hits})
    return {"stale_count": len(stale), "stale": stale}


